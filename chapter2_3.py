print(a.index('4')) #문자 4이 처음으로 나오는 위치를 반환해준다. 
print(a.index(2))

a=[1,2,2,3]
print(a.count(2)) #해당요소가 몇개나 있는지를 반환한다
a.extend([4,5,6]) #이함수에는 반드시 리스트만 포함가능하며, 이것으로 리스트를 확장시켜준다.
#즉 extend 함수는  a=a+[list] 와 동일한 효과를 지닌다!!
print(a)

