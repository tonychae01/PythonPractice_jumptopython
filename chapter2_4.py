#tuple 자료형 --> 순서가 있다!!!!
#list =[] tuple=()이용 리스트와 거의 비슷하지만 다른점:immutable 즉,값의 수정과 삭제 생성이 불가능!
#tuple의 핵심: 값을 바꿀수 없다. 즉, 프로그램의 실행동안 값이 변하지 않기를 바라면 튜플사용!!

t1=() #empty tuple
t2=(1,) #혼자 쓰일때는 반드시 콤마를 찍어야한다!!!
t3=(1,2,3)
t4=1,2,3 #괄호 생략가능
t5=(1,2,4,('ab',3))
print(t5[3][0])

#인덱싱,슬라이싱,더하기, 곱하기는 모두가능!!!
print(t5[3][0]) #인덱싱
print(t4[:2]) #슬라이싱
print(t1+t2) #더하기
print(t4*4) #곱하기